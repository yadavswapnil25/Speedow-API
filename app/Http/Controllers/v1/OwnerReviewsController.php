    }


    public function getMyReviews(Request $request)
    {
        $validator = Validator::make($request->all(), [
            'id' => 'required',
        ]);
        if ($validator->fails()) {
            $response = [
                'success' => false,
                'message' => 'Validation Error.',
                $validator->errors(),
                'status' => 500
            ];
            return response()->json($response, 404);
        }
        $data = OwnerReviews::where('freelancer_id', $request->id)->get();
        foreach ($data as $loop) {
            $loop->user = User::select('id', 'cover', 'first_name', 'last_name')->where(['id' => $loop->uid])->first();
        }
        $response = [
            'data' => $data,
            'success' => true,
            'status' => 200,
        ];
        return response()->json($response, 200);
    }
