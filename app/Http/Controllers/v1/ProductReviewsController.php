    public function getMyProductReviews(Request $request)
    {
        $validator = Validator::make($request->all(), [
            'id' => 'required',
        ]);
        if ($validator->fails()) {
            $response = [
                'success' => false,
                'message' => 'Validation Error.',
                $validator->errors(),
                'status' => 500
            ];
            return response()->json($response, 404);
        }

        $data = DB::table('products')
            ->select(
                'products.id as id',
                'products.name as name',
                'products.freelacer_id as freelacer_id',
                'product_reviews.notes as notes',
                'product_reviews.rating as rating',
                'product_reviews.uid as uid',
                'users.first_name as first_name',
                'users.last_name as last_name',
                'users.cover as user_cover',
                'products.cover as product_cover',
                'product_reviews.created_at as created_at'
            )
            ->join('product_reviews', 'products.id', 'product_reviews.product_id')
            ->join('users', 'product_reviews.uid', 'users.id')
            ->where('products.freelacer_id', $request->id)
            ->get();


        $response = [
            'data' => $data,
            'success' => true,
            'status' => 200,
        ];
        return response()->json($response, 200);
    }

