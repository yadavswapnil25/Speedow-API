<?php

namespace App\Http\Controllers\v1;

use App\Http\Controllers\Controller;
use Illuminate\Http\Request;
use App\Models\OwnerReviews;
use App\Models\User;
use App\Models\Salon;
use App\Models\Individual;
use Validator;

class OwnerReviewsController extends Controller
{
    public function save(Request $request)
    {
        $validator = Validator::make($request->all(), [
            'uid' => 'required',
            'freelancer_id' => 'required',
            'notes' => 'required',
            'rating' => 'required',
            'status' => 'required'
        ]);
        if ($validator->fails()) {
            $response = [
                'success' => false,
                'message' => 'Validation Error.',
                $validator->errors(),
                'status' => 500
            ];
            return response()->json($response, 404);
        }

        $data = OwnerReviews::create($request->all());
        if (is_null($data)) {
            $response = [
                'data' => $data,
                'message' => 'error',
                'status' => 500,
            ];
            return response()->json($response, 200);
        }
        $response = [
            'data' => $data,
            'success' => true,
            'status' => 200,
        ];
        return response()->json($response, 200);
    }

    public function updateOwnerReviews(Request $request)
    {
        $validator = Validator::make($request->all(), [
            'id' => 'required',
            'total_rating' => 'required',
            'rating' => 'required',
        ]);
        if ($validator->fails()) {
            $response = [
                'success' => false,
                'message' => 'Validation Error.',
                $validator->errors(),
                'status' => 500
            ];
            return response()->json($response, 404);
        }
        Salon::where('uid', $request->id)->update(['total_rating' => $request->total_rating, 'rating' => $request->rating]);
        Individual::where('uid', $request->id)->update(['total_rating' => $request->total_rating, 'rating' => $request->rating]);
        $response = [
            'data' => 'success',
            'success' => true,
            'status' => 200,
        ];
        return response()->json($response, 200);
    }

    public function getOwnerReviews(Request $request)
